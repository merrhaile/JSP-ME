// A function in JavaScript is a block of code designed to perform a particular task. 
// it takes some input and return an output where there is some obvious relationship 
// between the input and the output. A JavaScript function is executed when "something" 
// invokes it (calls it).

function addNum(n1, n2) {
  return n1 + n2;
}

console.log(addNum(4, 5));
